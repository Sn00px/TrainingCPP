#include <iostream>
using namespace std;


int main() {
        double value1 = (double)7/12;
        std::cout << value1 << '\n';

        int value2 = 7.3;
        cout << value2 << endl;

        int value3 = 8;
        value3 += 1; // value3 = value3 + 1 or value3++
        cout << value3 << endl;

        int value4 = 10;
        value4 /= 5;
        std::cout << value4 << '\n';

        //Mod operator
        int value5 = 13%5;
        cout << value5 << endl;

        double equation = 5.3/4+2*6;
        cout << equation << endl;


        return 0;
}
